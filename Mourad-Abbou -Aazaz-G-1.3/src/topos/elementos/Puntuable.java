package topos.elementos;

/**
 * Esta interfaz se encarga de implementar un metodo que devuelva el valor de la
 * puntuacion de cada elemento puntuable cuando es disparado.
 * 
 * @author Morad
 *
 */
public interface Puntuable {
	/**
	 * 
	 * @return Devuelve el valor de la puntuacion de cada elemento puntuable
	 *         cuando es disparado.
	 */
	public int getPuntuacion();
}
